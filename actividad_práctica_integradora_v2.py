import ctypes
import numpy as np

gpu_lib = ctypes.CDLL('./libgpu.so')
gpu_lib.process_array.argtypes = [
    ctypes.POINTER(ctypes.c_float),
    ctypes.c_int,
    ctypes.c_float,
    ctypes.c_float
]

def run_gpu_normalization(data_array):
    arr = np.array(data_array, dtype=np.float32)
    min_val, max_val = float(arr.min()), float(arr.max())
    n = len(arr)

    ptr = arr.ctypes.data_as(ctypes.POINTER(ctypes.c_float))
    gpu_lib.process_array(ptr, n, min_val, max_val)

    np.savetxt("preprocessed_data.csv", arr, delimiter=",", fmt="%.6f")
    return "preprocessed_data.csv"


# --- 1. ACTOR DE VALIDACIÓN ---
class ValidationActor(pykka.ThreadingActor):
    def validate(self, data):
        if not data or len(data) == 0:
            raise ValueError("El array de entrada está vacío.")
        print(f"[ValidationActor] Datos validados correctamente ({len(data)} elementos).")
        return True

# --- 2. ACTOR GPU SERVERLESS ---
class GPULambdaActor(pykka.ThreadingActor):
    def process_gpu(self, data, retries=3):
        for attempt in range(1, retries + 1):
            try:
                print(f"[GPULambdaActor] Intento {attempt}: Ejecutando Kernel CUDA/OpenMP...")
                if random.random() < 0.1 and attempt == 1:
                    raise ConnectionError("Simulación de fallo de red/Cold-start serverless.")

                output_file = run_gpu_normalization(data)
                print(f"[GPULambdaActor] Normalización exitosa en GPU -> {output_file}")
                return output_file
            except Exception as e:
                print(f"[GPULambdaActor] Error: {e}. Reintentando...")
                if attempt == retries:
                    raise e

# --- 3. ACTOR SPARK ---
class SparkLauncherActor(pykka.ThreadingActor):
    def launch_job(self, input_csv):
        print(f"[SparkLauncherActor] Ejecutando Jobs de Spark sobre {input_csv}...")

        # Reutilizar la sesión activa de Spark
        spark_session = SparkSession.builder.getOrCreate()

        # --- PIPELINE 1: RDD ---
        start_rdd = time.time()
        rdd = spark_session.sparkContext.textFile(input_csv)
        rdd_parsed = rdd.map(lambda line: float(line))
        rdd_sum = rdd_parsed.reduce(lambda a, b: a + b)
        rdd_count = rdd_parsed.count()
        rdd_mean = rdd_sum / rdd_count
        time_rdd = time.time() - start_rdd

        # --- PIPELINE 2: DATAFRAME ---
        start_df = time.time()
        df = spark_session.read.csv(input_csv, inferSchema=True).toDF("val")
        df_result = df.groupBy().avg("val").collect()
        df_mean = df_result[0][0]
        time_df = time.time() - start_df

        speedup = time_rdd / time_df if time_df > 0 else 0.0

        return {
            "rdd_time_sec": round(time_rdd, 4),
            "df_time_sec": round(time_df, 4),
            "speedup": round(speedup, 2),
            "rdd_mean": float(rdd_mean),
            "df_mean": float(df_mean)
        }

# --- 4. ACTOR ORQUESTADOR PRINCIPAL ---
class JobManagerActor(pykka.ThreadingActor):
    def __init__(self):
        super().__init__()
        self.validator = ValidationActor.start()
        self.gpu_actor = GPULambdaActor.start()
        self.spark_actor = SparkLauncherActor.start()

    def execute_pipeline(self, raw_data):
        self.validator.proxy().validate(raw_data).get()
        csv_file = self.gpu_actor.proxy().process_gpu(raw_data).get()
        spark_metrics = self.spark_actor.proxy().launch_job(csv_file).get()

        return {
            "status": "SUCCESS",
            "processed_elements": len(raw_data),
            "metrics": spark_metrics
        }

    def on_stop(self):
        self.validator.stop()
        self.gpu_actor.stop()
        self.spark_actor.stop()

